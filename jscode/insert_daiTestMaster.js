$('#testMasterForm').on('submit', function(e) {
    e.preventDefault();
    var returnVal = $("#testMasterForm").valid();
    if (returnVal) {
        var fData = new FormData(this);
        fData.append('suserid',data.userId);
        fData.append('susername',data.username);
        $.ajax({
            url: url + 'insert_DaiTest_Master.php',
            type: 'POST',
            data: fData,
            cache: false,
            contentType: false,
            processData: false,
            dataType: 'json',
            success: function(response) {
                if (response.Responsecode == 200) {
                    swal({
                        position: 'top-end',
                        icon: 'success',
                        title: response.Message,
                        button: false,
                        timer: 1500
                    });
                    $('#testMasterForm').trigger('reset');
                    $('#cButton').click();
                    $('#testMasterForm').trigger('reset');
                    testes.set(response.Data.testId, response.Data);
                    listTest(testes);
                } else {
                    swal({
                        position: 'top-end',
                        icon: 'warning',
                        title: response.Message,
                        button: false,
                        timer: 1500
                    });
               
                }
            }
        });
    }
});
